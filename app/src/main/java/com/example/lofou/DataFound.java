package com.example.lofou;

    public class DataFound {
        private String NamaBarang;
        private String WaktuDitemukan;
        private String Lokasi;
        private String Deskripsi;
        private String ImageID;

        public DataFound() {
        }

        public String getNamaBarang() {
            return NamaBarang;
        }

        public void setNamaBarang(String namaBarang) {
            NamaBarang = namaBarang;
        }

        public String getWaktuDitemukan() {
            return WaktuDitemukan;
        }

        public void setWaktuDitemukan(String waktuDitemukan) {
            WaktuDitemukan = waktuDitemukan;
        }

        public String getLokasi() {
            return Lokasi;
        }

        public void setLokasi(String lokasi) {
            Lokasi = lokasi;
        }

        public String getDeskripsi() {
            return Deskripsi;
        }

        public void setDeskripsi(String deskripsi) {
            Deskripsi = deskripsi;
        }

        public String getImageID() {
            return ImageID;
        }

        public void setImageID(String imageID) {
            ImageID = imageID;
        }
    }

